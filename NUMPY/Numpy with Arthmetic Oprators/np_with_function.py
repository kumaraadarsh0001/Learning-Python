import numpy as np
'Aditon'
var=np.array([12,13,14,15,20])
a=2
ad=np.add(var,a)
print('\nAditon',ad)
'Substraction'
var=np.array([12,13,14,15,20])
b=5
sb=np.subtract(var,b)
print('Substraction',sb)
'multiplication'
var=np.array([12,13,14,15,20])
m=5
mm=np.multiply(var,m)
print('Multiplication',mm)
'Division'
var=np.array([12,13,14,15,20])
n=2
mm=np.divide(var,n)
print('Division',mm)
'Florr Devision'
var=np.array([12,13,14,15,20])
f=5
fd=np.floor_divide(var,f)
print('Florr Devision',fd)
'Modulous'
var=np.array([12,13,14,15,20])
md=5
modd=np.mod(var,md)
print('Modulous',modd)
'Reciprocal'
var=np.array([1,3,4,2])
res=np.reciprocal(var)
print('Reciprocal',res)