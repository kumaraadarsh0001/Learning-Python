#string function chek
print("#string function is upper")
a='KUMAR  AADARSH'
b='aadarsh kumar'
c='Kumar  Aadarsh'
name1=a.isupper()
name2=b.isupper()
name3=c.isupper()
print(name1)
print(name2)
print(name3)

print("#string is lower")
